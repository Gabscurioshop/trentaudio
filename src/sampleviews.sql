CREATE VIEW MD_EDIT AS
SELECT REPORT_NUM, USR_EMAIL FROM REPORT
WHERE type = 'md_edit';
SELECT * FROM (MD_EDIT NATURAL JOIN METADATA_EDIT_REQUEST);

